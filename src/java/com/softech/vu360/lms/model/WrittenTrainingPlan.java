	public TPQuestion searchQuestion(String strText){
		TPQuestion question = null;
		
		if(strText.equals("Based on Registered Representative"))
			System.out.println(strText);
		for(Method method : this.getClass().getDeclaredMethods()){
			if(method.getName().contains("getQuestion")){
				try {
					question = (TPQuestion)method.invoke(this);
					if(question.getQuestion().contains(strText)){
						break;
					}
				} catch (Exception e) {
					e.printStackTrace();
				}
			}
		}
		return question;
	}
	
	public TPQuestion getExactQuestion(String strText){
		TPQuestion question = null;
		int i=1;
		for(Method method : this.getClass().getDeclaredMethods()){
			if(method.getName().equalsIgnoreCase("getQuestion"+i)){
				try {
					question = (TPQuestion)method.invoke(this);
					if(question!=null){
						if(question.getQuestion()!=null && question.getQuestion().contains(strText)){
							break;
						}
					}
					
				} catch (Exception e) {
					e.printStackTrace();
				}
				i++;
			}
		}
		return question;
	}
	
	public  Boolean isAnswerExists(String strText){
		 Boolean exists = false;
		TPQuestion question = null;
		
		for(Method method : this.getClass().getDeclaredMethods()){
			if(method.getName().contains("getQuestion")){
				try {
					question = (TPQuestion)method.invoke(this);
					if(question.getAnswerText(strText)){
						exists= true;
						break;
					}
				} catch (Exception e) {
					e.printStackTrace();
				}
			}
		}
		return exists;
	}
